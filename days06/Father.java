public class Father
{
	//field
	public String name = "홍길동";  // 누구나 접근 가능
	protected String bank = "한양은행";  // protected = 상속 관계 같은 package 접근가능
	String branch = "역삼동지점"; // (         ) : 같은 package만 접근가능
	private int password = 1234; // private = 자신만 접근 가능

	//Constructor
	public Father(){
	}

	//method
	/*
		각각의 field 아래의 method를 통해 접근 가능
		아래의 각각의 getter method의 의미를 이해
	*/

	public String getBank(int pwd){
		if(pwd != 7777){
			return "key값은 정확히 입력하세요";
		}else{
			return this.bank;
		}
	}


//은행 비밀번호를 알기위한 key를 알고 있으면 password를 return
	public int getPassword(int key){
		if(key != 7777){
			return 0;
		}else{
			return this.password;
		}
	}
	public String getName(){
		return name;
	}
	public String getBranch(){
		return branch;
	}
}



